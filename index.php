            "img/cafe1.jpg",
            "img/cafe2.jpg"
        ]
    };

    const modal = document.getElementById("galeriaModal");
    const imagenGrande = document.getElementById("imagenGrande");
    const cerrar = document.querySelector(".cerrar-galeria");
    const prev = document.getElementById("prev");
    const next = document.getElementById("next");

    let galeriaActual = [];
    let indiceActual = 0;

    document.querySelectorAll(".inst-item").forEach(item => {
        item.addEventListener("click", function() {
            const tipo = this.getAttribute("data-galeria");

            if (!galerias[tipo]) return;

            galeriaActual = galerias[tipo];
            indiceActual = 0;
            imagenGrande.src = galeriaActual[indiceActual];
            modal.style.display = "flex";
        });
    });

    next.addEventListener("click", function() {
        indiceActual = (indiceActual + 1) % galeriaActual.length;
        imagenGrande.src = galeriaActual[indiceActual];
    });

    prev.addEventListener("click", function() {
        indiceActual = (indiceActual - 1 + galeriaActual.length) % galeriaActual.length;
        imagenGrande.src = galeriaActual[indiceActual];
    });

    cerrar.addEventListener("click", function() {
        modal.style.display = "none";
    });

    modal.addEventListener("click", function(e) {
        if (e.target === modal) {
            modal.style.display = "none";
        }
    });

});
</script>
